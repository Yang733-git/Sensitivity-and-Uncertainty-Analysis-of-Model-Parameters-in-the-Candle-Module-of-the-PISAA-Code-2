# ===================== 功能块：读取样本文件（保留注释行，严格解析数据行） =====================  # 功能块说明：只把真正的数值行解析为样本，避免把异常行误算成数据
def load_samples(file_path):  # 定义函数：读取样本文件，返回（注释行列表，数据 DataFrame）
    with open(file_path, "r", encoding="utf-8") as file:  # 以 UTF-8 文本方式打开文件进行读取
        lines = file.readlines()  # 一次性读取全部行到内存中

    comment_lines = []  # 初始化注释行列表，用于保留原文件中的注释内容
    numeric_rows = []  # 初始化数值行列表，用于存放解析后的样本数据
    expected_cols = None  # 初始化列数基准，后续用于检查每一行的列数是否一致

    for line_no, raw_line in enumerate(lines, start=1):  # 逐行遍历原始文本，并记录从 1 开始的行号
        stripped_line = raw_line.strip()  # 去掉首尾空白字符，便于判断空行、注释行和数据行

        if stripped_line == "":  # 如果这一行去掉空白后为空，说明它是空行
            continue  # 直接跳过空行，不把它当作样本，也不写回输出

        if stripped_line.startswith("#"):  # 如果这一行在去掉首尾空白后以 # 开头，说明它是注释行
            comment_lines.append(raw_line if raw_line.endswith("\n") else raw_line + "\n")  # 原样保留注释行，并确保末尾带换行
            continue  # 注释行不参与后续数值解析，继续处理下一行

        parts = stripped_line.split("\t")  # 对非注释行按制表符分割，要求输入样本文件使用制表符分隔
        try:  # 尝试把这一行的每个字段都解析为浮点数
            row = [float(item) for item in parts]  # 将当前行的所有字段转换为浮点数列表
        except ValueError as exc:  # 如果任意字段无法转成浮点数，就说明这一行不是合法样本行
            raise ValueError(f"文件 {file_path} 的第 {line_no} 行不是纯数值数据行：{raw_line.rstrip()}") from exc  # 抛出带文件名和行号的明确异常，防止静默误读

        if expected_cols is None:  # 如果这是遇到的第一行合法数据
            expected_cols = len(row)  # 记录这一行的列数，作为后续所有数据行的列数基准
        elif len(row) != expected_cols:  # 如果后续数据行的列数和第一行不一致
            raise ValueError(f"文件 {file_path} 的第 {line_no} 行列数为 {len(row)}，与前面数据行列数 {expected_cols} 不一致。")  # 直接报错，防止脏数据混入导致结果异常

        numeric_rows.append(row)  # 将已经成功解析的数值行加入样本列表

    if not numeric_rows:  # 如果整个文件中没有解析出任何有效样本行
        raise ValueError(f"文件 {file_path} 中没有读取到任何有效的数值样本行。")  # 抛出异常，提醒输入文件内容有问题

    data = pd.DataFrame(numeric_rows)  # 将数值样本列表转换成 DataFrame，便于后续统一处理
    return comment_lines, data  # 返回注释行列表和样本数据表


