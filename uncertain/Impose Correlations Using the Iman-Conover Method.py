# ===================== 功能块：Iman–Conover 秩重排植入相关 =====================                 # 功能块说明：保持每列取值集合不变，仅通过重排样本顺序逼近目标相关
def iman_conover_transform(samples, target_matrix):  # 定义函数：对输入样本执行 Iman–Conover 相关植入
    X = np.asarray(samples, dtype=float)  # 将输入样本转换为浮点型二维数组，保证后续运算稳定
    n, d = X.shape  # 读取样本的行数 n 和列数 d

    if n < 2:  # 如果样本数小于 2，就无法计算有意义的列相关
        raise ValueError("样本行数必须至少为 2，才能执行相关植入。")  # 抛出明确错误，防止后续相关矩阵计算失败

    if target_matrix.ndim != 2:  # 检查目标相关矩阵必须是二维矩阵
        raise ValueError("目标相关矩阵必须是二维数组。")  # 如果不是二维矩阵，直接报错

    if target_matrix.shape[0] != target_matrix.shape[1]:  # 检查目标相关矩阵必须是方阵
        raise ValueError("目标相关矩阵必须是方阵。")  # 如果不是方阵，直接报错

    if target_matrix.shape[0] != d:  # 检查目标相关矩阵的维度必须和样本列数一致
        raise ValueError(f"样本列数为 {d}，但目标相关矩阵维度为 {target_matrix.shape[0]}×{target_matrix.shape[1]}，两者不一致。")  # 维度不匹配时立即报错

    target_matrix = 0.5 * (target_matrix + target_matrix.T)  # 对目标相关矩阵做一次对称化，消除数值层面的轻微非对称

    def _norm_ppf(u):  # 定义内部函数：标准正态分布的反函数近似，用于把分位数映射到正态分数
        a = [  # 定义 Acklam 近似公式中心区分子的系数
            -3.969683028665376e+01,  # 系数 a0
             2.209460984245205e+02,  # 系数 a1
            -2.759285104469687e+02,  # 系数 a2
             1.383577518672690e+02,  # 系数 a3
            -3.066479806614716e+01,  # 系数 a4
             2.506628277459239e+00,  # 系数 a5
        ]  # 系数数组 a 结束
        b = [  # 定义 Acklam 近似公式中心区分母的系数
            -5.447609879822406e+01,  # 系数 b0
             1.615858368580409e+02,  # 系数 b1
            -1.556989798598866e+02,  # 系数 b2
             6.680131188771972e+01,  # 系数 b3
            -1.328068155288572e+01,  # 系数 b4
        ]  # 系数数组 b 结束
        c = [  # 定义 Acklam 近似公式尾部分子的系数
            -7.784894002430293e-03,  # 系数 c0
            -3.223964580411365e-01,  # 系数 c1
            -2.400758277161838e+00,  # 系数 c2
            -2.549732539343734e+00,  # 系数 c3
             4.374664141464968e+00,  # 系数 c4
             2.938163982698783e+00,  # 系数 c5
        ]  # 系数数组 c 结束
        d_coef = [  # 定义 Acklam 近似公式尾部分母的系数
             7.784695709041462e-03,  # 系数 d0
             3.224671290700398e-01,  # 系数 d1
             2.445134137142996e+00,  # 系数 d2
             3.754408661907416e+00,  # 系数 d3
        ]  # 系数数组 d 结束

        u = np.asarray(u, dtype=float)  # 将输入分位数数组转成浮点数组
        eps = np.finfo(float).eps  # 读取浮点机器精度，用于避免分位数取到 0 或 1
        u = np.clip(u, eps, 1.0 - eps)  # 将分位数限制在 (eps, 1-eps) 范围内，避免返回正负无穷
        pl = 0.02425  # 定义左尾分段阈值
        pr = 1.0 - pl  # 定义右尾分段阈值
        x = np.empty_like(u)  # 创建与输入同形状的输出数组，用于存放正态分位值

        mask_l = u < pl  # 构造左尾区间的布尔掩码
        if np.any(mask_l):  # 如果存在左尾元素
            q = np.sqrt(-2.0 * np.log(u[mask_l]))  # 按 Acklam 公式计算左尾辅助变量 q
            x[mask_l] = (((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) / ((((d_coef[0] * q + d_coef[1]) * q + d_coef[2]) * q + d_coef[3]) * q + 1.0)  # 计算左尾近似值
            x[mask_l] = x[mask_l]  # 左尾分支在本实现中直接保留计算结果，不额外翻符号

        mask_r = u > pr  # 构造右尾区间的布尔掩码
        if np.any(mask_r):  # 如果存在右尾元素
            q = np.sqrt(-2.0 * np.log(1.0 - u[mask_r]))  # 按 Acklam 公式计算右尾辅助变量 q
            x[mask_r] = -(((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) / ((((d_coef[0] * q + d_coef[1]) * q + d_coef[2]) * q + d_coef[3]) * q + 1.0)  # 计算右尾近似值

        mask_c = (~mask_l) & (~mask_r)  # 构造中心区间的布尔掩码
        if np.any(mask_c):  # 如果存在中心区元素
            q = u[mask_c] - 0.5  # 计算中心区辅助变量 q
            r = q * q  # 计算 r=q^2，供多项式展开使用
            x[mask_c] = (((((a[0] * r + a[1]) * r + a[2]) * r + a[3]) * r + a[4]) * r + a[5]) * q / (((((b[0] * r + b[1]) * r + b[2]) * r + b[3]) * r + b[4]) * r + 1.0)  # 计算中心区近似值

        return x  # 返回标准正态分布反函数的近似结果

    def _chol_with_jitter(matrix, base=1e-10, tries=6):  # 定义内部函数：对近似正定矩阵做带抖动的 Cholesky 分解
        m = 0.5 * (matrix + matrix.T)  # 先把输入矩阵对称化，减少数值误差带来的分解失败
        for k in range(tries):  # 最多尝试指定次数
            try:  # 尝试直接进行 Cholesky 分解
                return np.linalg.cholesky(m)  # 如果成功，返回下三角矩阵
            except np.linalg.LinAlgError:  # 如果失败，说明矩阵还不够正定
                m = m + np.eye(m.shape[0]) * (base * (10.0 ** k))  # 在对角线上逐步增加抖动项后重试
        raise np.linalg.LinAlgError("目标相关矩阵或中间相关矩阵无法进行 Cholesky 分解，请检查其正定性。")  # 多次尝试后仍失败，则抛出异常

    ranks = np.empty_like(X, dtype=float)  # 创建与样本同形状的数组，用于存放每列的秩
    for j in range(d):  # 逐列处理样本数据
        series = pd.Series(X[:, j])  # 将当前列转成 pandas Series，便于调用 rank 方法
        ranks[:, j] = series.rank(method="average").to_numpy(dtype=float)  # 计算平均秩，并转为浮点型 NumPy 数组保存

    U = ranks / (n + 1.0)  # 将秩映射到 (0,1) 开区间中的分位数，避免出现 0 和 1
    Z = _norm_ppf(U)  # 将分位数进一步映射到标准正态分数空间

    if np.isnan(Z).any() or np.isinf(Z).any():  # 如果正态分数中出现 NaN 或 Inf，说明前面的映射出了问题
        raise ValueError("标准正态分数计算结果中出现 NaN 或 Inf，请检查输入数据。")  # 明确报错，避免后续矩阵运算产生更隐蔽的问题

    Rz = np.corrcoef(Z.T)  # 计算正态分数矩阵各列之间的样本相关矩阵
    Lz = _chol_with_jitter(Rz)  # 对当前样本相关矩阵做带抖动的 Cholesky 分解
    Lt = _chol_with_jitter(target_matrix)  # 对目标相关矩阵做带抖动的 Cholesky 分解
    A = np.linalg.solve(Lz, Lt)  # 解线性方程 Lz @ A = Lt，得到相关映射矩阵 A
    Yz = Z @ A  # 在正态分数空间中施加相关变换，得到新的相关结构

    Y = np.empty_like(X, dtype=float)  # 创建输出数组，大小与原样本完全一致，确保行数不会变化
    for j in range(d):  # 逐列按照新分数的秩，对原始样本值进行稳定重排
        order = np.argsort(Yz[:, j], kind="mergesort")  # 获取变换后分数从小到大的稳定排序索引
        x_sorted = np.sort(X[:, j], kind="mergesort")  # 对原始该列数值做从小到大的稳定排序
        Y[order, j] = x_sorted  # 按照 Yz 的秩顺序把原始数值重新放回对应位置，从而只改顺序不改取值集合

    if Y.shape[0] != X.shape[0]:  # 再做一次硬校验，确认处理前后行数没有变化
        raise RuntimeError(f"处理前样本行数为 {X.shape[0]}，处理后样本行数为 {Y.shape[0]}，这不应该发生。")  # 如果行数变化，立即终止并报错

    return Y  # 返回处理后的样本矩阵


# ===================== 功能块：保存处理后的样本（保留注释） =====================                  # 功能块说明：按原文件格式写出，先写注释，再写样本，制表符分隔，小数 4 位
def save_samples(comment_lines, samples, file_path):  # 定义函数：将注释行和处理后的样本一起写到目标文件
    sample_array = np.asarray(samples, dtype=float)  # 将输入样本统一转为浮点型数组，保证写出时格式一致

    with open(file_path, "w", encoding="utf-8") as file:  # 以写入模式打开目标文件，覆盖原内容
        file.writelines(comment_lines)  # 先把原始注释行按顺序写回输出文件

        for row in sample_array:  # 逐行遍历样本数组，手工写出，避免任何隐式表头或附加内容
            formatted_row = "\t".join(f"{value:.4f}" for value in row)  # 将当前行每个浮点数格式化为 4 位小数，并用制表符拼接
            file.write(formatted_row + "\n")  # 将当前格式化后的样本行写入文件，并补一个换行符


# ===================== 功能块：主流程（批量执行三份文件） =====================                    # 功能块说明：路径保持不变，逐一读取、处理、校验并输出三份文件
def main():  # 定义主函数
    file_paths = [  # 定义三个输入文件路径，保持与你原脚本完全一致
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_samples_UO2.txt",  # 第 1 个输入文件路径
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_samples_CL.txt",  # 第 2 个输入文件路径
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_samples_CR.txt",  # 第 3 个输入文件路径
    ]  # 输入路径列表定义结束

    output_paths = [  # 定义三个输出文件路径，保持与你原脚本完全一致
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_transformed_UO2.txt",  # 第 1 个输出文件路径
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_transformed_CL.txt",  # 第 2 个输出文件路径
        r"C:\Users\11201\Desktop\sensities\uncertain\uncertain_transformed_CR.txt",  # 第 3 个输出文件路径
    ]  # 输出路径列表定义结束

    target_matrix = load_target_matrix()  # 读取目标相关矩阵，供三个文件共用

    for file_path, output_path in zip(file_paths, output_paths):  # 将输入输出路径成对遍历，逐个文件处理
        comment_lines, samples = load_samples(file_path)  # 读取当前文件的注释和数值样本
        input_row_count = samples.shape[0]  # 记录输入样本的行数，用于后续一致性校验
        transformed_samples = iman_conover_transform(samples.values, target_matrix)  # 对当前样本执行 Iman–Conover 相关植入
        output_row_count = transformed_samples.shape[0]  # 记录处理后样本的行数

        if output_row_count != input_row_count:  # 硬校验：如果处理前后样本行数不相等
            raise RuntimeError(f"文件 {file_path} 处理前样本数为 {input_row_count}，处理后样本数为 {output_row_count}，已中止写出。")  # 直接终止，防止写出错误文件

        save_samples(comment_lines, transformed_samples, output_path)  # 将注释和处理后的样本写入目标文件
        print(f"Processed and saved: {output_path} | input_rows={input_row_count} | output_rows={output_row_count}")  # 输出处理结果与行数校验信息

    return None  # 主函数执行结束，显式返回 None


if __name__ == "__main__":  # 判断当前文件是否作为主程序直接运行
    main()  # 如果是主程序运行，则执行主流程